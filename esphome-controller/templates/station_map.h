const int STATION_MAP_SIZE = sizeof(STATION_MAP) / sizeof(STATION_MAP[0]);

int lookup_unified_id(int nextStaId, const std::string& line) {
    for (int i = 0; i < STATION_MAP_SIZE; i++) {
        if (STATION_MAP[i].nextStaId == nextStaId && STATION_MAP[i].line == line) {
            return STATION_MAP[i].unifiedId;
        }
    }
    return -1;
}

